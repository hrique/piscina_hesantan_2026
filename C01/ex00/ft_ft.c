void	ft_ft(int *nbr)
{
	*nbr = 42;
}

int	main(void)
{
	int	nbr;
	int	dez;
	int	uni;

	nbr = 0;
	ft_ft(&nbr);
	dez = (nbr / 10 + '0');
	uni = (nbr % 10 + '0');
	write(1, &dez, 1);
	write(1, &uni, 1);
	write(1, "\n", 1);
	return (0);
}
